package com.example;

public class Clase {
    public static void main(String[] args) throws Exception {

        String texto = "\nRecordatorio: Los tipos de datos más comunes son int, long, double, boolean, String.";
        System.out.println(texto);

        int numeroInt = 10;
        System.out.println("numero int = " + numeroInt);

        long numeroLong = 100300005;
        System.out.println("numero long = " + numeroLong);

        double numeroDouble = 10.9;
        System.out.println("numero double = " + numeroDouble);

        boolean verdadero = true;
        System.out.println("Camilo está casado = " + verdadero);

        String texto1 = "Adios";
        System.out.println(texto1);
    }
}
